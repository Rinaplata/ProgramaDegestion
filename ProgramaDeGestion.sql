CREATE VIEW [dbo].[VW_EMPLEADO]
AS
SELECT 
EMPLEADO.NOMBRE,
EMPLEADO.CEDULA,
CARGO.SALARIO,
PROYECTO.DESCRIPCION 
FROM EMPLEADO INNER JOIN CARGO
